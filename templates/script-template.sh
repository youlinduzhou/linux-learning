set -e  # 遇到错误立即退出

# 颜色输出定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'  # No Color

# ==============================================================================
# 函数：log_info
# 功能：输出信息日志
# ==============================================================================
log_info() {
    echo -e "${GREEN}[INFO]${NC} $1"
}

# ==============================================================================
# 函数：log_warn
# 功能：输出警告日志
# ==============================================================================
log_warn() {
    echo -e "${YELLOW}[WARN]${NC} $1"
}

# ==============================================================================
# 函数：log_error
# 功能：输出错误日志
# ==============================================================================
log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# ==============================================================================
# 主程序
# ==============================================================================
main() {
    log_info "脚本开始执行"
    
    # 你的代码在这里
    
    log_info "脚本执行完成"
}

# 启动主程序
main "$@"
