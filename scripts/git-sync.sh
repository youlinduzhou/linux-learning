#!/bin/bash

# ==============================================================================
# 脚本名称：git-sync.sh
# 功能描述：一键同步学习产出到 GitHub
# 使用方法：./git-sync.sh "提交信息"
# 创建日期：2026-05-07
# 作者：youlinduzhou
# 版本：v1.0
# ==============================================================================

set -e  # 遇到错误立即退出

# 颜色输出定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
NC='\033[0m'  # No Color

# ==============================================================================
# 函数：log_info
# 功能：输出信息日志
# ==============================================================================
log_info() {
    echo -e "${CYAN}[INFO]${NC} $1"
}

# ==============================================================================
# 函数：log_success
# 功能：输出成功日志
# ==============================================================================
log_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

# ==============================================================================
# 函数：log_warn
# 功能：输出警告日志
# ==============================================================================
log_warn() {
    echo -e "${YELLOW}[WARN]${NC} $1"
}

# ==============================================================================
# 函数：log_error
# 功能：输出错误日志
# ==============================================================================
log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# ==============================================================================
# 函数：show_banner
# 功能：显示欢迎横幅
# ==============================================================================
show_banner() {
    echo ""
    echo -e "${CYAN}╔═══════════════════════════════════════════════════════════════╗${NC}"
    echo -e "${CYAN}║              云原生DevOps学习 - Git同步工具                     ║${NC}"
    echo -e "${CYAN}╚═══════════════════════════════════════════════════════════════╝${NC}"
    echo ""
}

# ==============================================================================
# 函数：check_git
# 功能：检查是否为 Git 仓库
# ==============================================================================
check_git() {
    log_info "检查 Git 仓库状态..."
    if [ ! -d ".git" ]; then
        log_error "当前目录不是 Git 仓库！"
        log_info "请先执行: git init"
        exit 1
    fi
    log_success "Git 仓库检查通过"
}

# ==============================================================================
# 函数：check_remote
# 功能：检查远程仓库配置
# ==============================================================================
check_remote() {
    log_info "检查远程仓库配置..."
    if ! git remote | grep -q "origin"; then
        log_warn "未配置远程仓库 origin"
        log_info "请执行: git remote add origin <仓库地址>"
    else
        log_success "远程仓库配置正常"
    fi
}

# ==============================================================================
# 主程序
# ==============================================================================
main() {
    show_banner
    
    # 参数检查
    if [ $# -eq 0 ]; then
        log_error "请提供提交信息！"
        echo ""
        echo -e "${YELLOW}使用方法：${NC}"
        echo -e "  $0 \"week01: Day1 文件系统探索\""
        echo -e "  $0 \"week01: 本周学习完成\""
        echo ""
        exit 1
    fi

    COMMIT_MSG="$1"
    log_info "提交信息：${YELLOW}${COMMIT_MSG}${NC}"
    
    # 检查 Git
    check_git
    
    # 检查远程仓库
    check_remote
    
    echo ""
    log_info "开始同步流程..."
    
    # 添加所有变更
    log_info "添加变更到暂存区..."
    git add .
    
    # 检查是否有变更
    if git diff --staged --quiet; then
        log_warn "没有变更需要提交！"
        exit 0
    fi
    
    # 提交
    log_info "提交变更..."
    git commit -m "${COMMIT_MSG}"
    log_success "提交成功"
    
    # 拉取远程变更（避免冲突）
    log_info "拉取远程变更..."
    if git pull origin main --rebase 2>/dev/null; then
        log_success "拉取远程变更完成"
    else
        log_warn "拉取远程变更失败，尝试继续推送"
    fi
    
    # 推送
    log_info "推送到远程仓库..."
    git push origin main
    log_success "推送完成！"
    
    echo ""
    log_success "═══════════════════════════════════════════════════════════════"
    log_success "  同步已完成！学习产出已上传至 GitHub"
    log_success "═══════════════════════════════════════════════════════════════"
    echo ""
}

# 启动主程序
main "$@"
